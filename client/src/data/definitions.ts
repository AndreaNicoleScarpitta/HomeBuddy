export function getDefinition(slug: string): DefinitionTerm | undefined {
  return definitions.find((d) => d.id === slug);
}

export function searchDefinitions(query: string): DefinitionTerm[] {
  if (!query.trim()) return definitions;
  const q = query.toLowerCase().trim();
  return definitions.filter(
    (d) =>
      d.title.toLowerCase().includes(q) ||
      d.shortDefinition.toLowerCase().includes(q) ||
      d.synonyms.some((s) => s.toLowerCase().includes(q))
  );
}

export function getDefinitionsByCategory(
  category: DefinitionCategory
): DefinitionTerm[] {
  return definitions.filter((d) => d.category === category);
}
